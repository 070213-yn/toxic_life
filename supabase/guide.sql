-- 準備ガイドのチェック項目（2人で編集可能）
CREATE TABLE guide_items (
  id UUID PRIMARY KEY DEFAULT gen_random_uuid(),
  section TEXT NOT NULL,      -- セクション名
  title TEXT NOT NULL,
  is_checked BOOLEAN DEFAULT FALSE,
  checked_by UUID REFERENCES profiles(id),
  memo TEXT,                  -- 2人で自由に書き込めるメモ
  sort_order INTEGER DEFAULT 0,
  created_at TIMESTAMPTZ DEFAULT NOW(),
  updated_at TIMESTAMPTZ DEFAULT NOW()
);

ALTER TABLE guide_items ENABLE ROW LEVEL SECURITY;
CREATE POLICY "認証済みユーザーはガイドを全操作可能" ON guide_items FOR ALL TO authenticated USING (true) WITH CHECK (true);
CREATE TRIGGER update_guide_items_updated_at BEFORE UPDATE ON guide_items FOR EACH ROW EXECUTE FUNCTION public.update_updated_at();

